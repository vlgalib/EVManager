// Функция для получения логотипа цепочки по имени
export const getChainLogo = (chainName: string): string | undefined => {
  if (!chainName) return undefined;
  
  // Сначала проверяем маппинг сокращенных названий
  const mappedName = chainNameMapping[chainName.toLowerCase()];
  const searchName = mappedName || chainName;
  
  // Ищем логотип по полному названию
  const chain = chainLogos.find(chain => 
    chain.name.toLowerCase() === searchName.toLowerCase()
  );
  
  return chain?.logo;
};

// Функция для получения данных цепочки по имени
export const getChainData = (chainName: string): ChainLogo | undefined => {
  if (!chainName) return undefined;
  
  // Сначала проверяем маппинг сокращенных названий
  const mappedName = chainNameMapping[chainName.toLowerCase()];
  const searchName = mappedName || chainName;
  
  // Ищем данные по полному названию
  return chainLogos.find(chain => 
    chain.name.toLowerCase() === searchName.toLowerCase()
  );
};

