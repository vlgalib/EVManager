// Обновленная функция для получения правильного названия сети
export const getChainDisplayName = (chainName: string): string => {
  if (!chainName) return '';
  
  // Сначала проверяем маппинг сокращенных названий
  const mappedName = chainNameMapping[chainName.toLowerCase()];
  if (mappedName) {
    return mappedName;
  }
  
  // Если нет в маппинге, ищем в chainLogos
  const chain = chainLogos.find(chain => 
    chain.name.toLowerCase() === chainName.toLowerCase()
  );
  
  return chain ? chain.name : chainName;
}; 